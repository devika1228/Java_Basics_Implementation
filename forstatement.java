class forstatement {

    // program to find the sum of natural numbers from 1 to 10
    public static void main(String[] args) {
        int sum = 0;
        int n = 1000;

        // for loop
        for (int i = 1; i <= n; i++)

        {
            sum += i;
        }
        System.out.println("Sum=" + sum);
    }

}
